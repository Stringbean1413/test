# five circle
import turtle

turtle.circle(50)

turtle.penup()
turtle.goto(120, 0)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(240, 0)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(60, -60)
turtle.pendown()
turtle.circle(50)

turtle.penup()
turtle.goto(180, -60)
turtle.pendown()
turtle.circle(50)
